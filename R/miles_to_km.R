#' Miles to km
#'
#' This function converts the values from miles to kilometers.
#'
#' @param x Numeric in miles
#'
#' @returns A value in km
#' @export
#'
#' @references Smith, J., 2024, Afdsfdsfs, ADfsds, doi:
#'
#' @details
#' If you use this and argument, this and that happens...
#'
#'
#' @examples
#' miles_to_km(2)
#' miles_to_km(0)
#' miles_to_km(100)
miles_to_km = function(x){
  x * 1.609
}
